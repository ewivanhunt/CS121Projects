package classActivity;

class Ethan {
    String name;
    int age;
    String family;

    public Ethan(String name, int age, String family) {
        this.name =name;
        this.age = age;
        this.family = family;
    }
    public void EthanInfo() {
        System.out.println("Name: " + name);
        System.out.println("Age: " + age);
        System.out.println("Family: " + family);
    }
}
